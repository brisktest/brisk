package shared

import (
	"crypto/rand"
	"encoding/hex"
)

func GetLastNBytes(n int, array []byte) []byte {
	if len(array) > n {
		return array[len(array)-n : len(array)-1]
	} else {
		return array
	}

}

func RandomHex(n int) (string, error) {
	bytes := make([]byte, n)
	if _, err := rand.Read(bytes); err != nil {
		return "", err
	}
	return hex.EncodeToString(bytes), nil
}
